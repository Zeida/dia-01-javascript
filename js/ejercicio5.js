// Ejercicio 5: Generar frases con array
// 
// Descripción:
// Crea una función que reciba un array de frutas y genere un nuevo array donde
// cada fruta esté dentro de una frase, usando un bucle for normal.
//
// Instrucciones:
// 1. Crea una función llamada generarFrases que reciba un array de frutas
// 2. Crea un nuevo array vacío para las frases
// 3. Usa un bucle for normal (no forEach) para recorrer el array
// 4. Por cada fruta, crea una frase "Fruta encontrada: X" donde X es la fruta
// 5. Agrega cada frase al nuevo array con push()
// 6. Retorna el array de frases
//
// Ejemplo:
// generarFrases(['manzana', 'pera', 'uva'])
// debe retornar ['Fruta encontrada: manzana', 'Fruta encontrada: pera', 'Fruta encontrada: uva']
//
// Pista: Usa array[i] para acceder a cada fruta y array.push() para agregar al nuevo array